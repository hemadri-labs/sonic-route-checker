"""
api.py — SONiC route consistency checker: FastAPI REST server

Exposes collector + diff engine results as REST endpoints.
The OpenAPI schema auto-generated by FastAPI is directly usable by
LangGraph for tool definitions.

Environment variables:
    SONIC_HOST      Redis host (default: 127.0.0.1)
    SONIC_REDIS_PORT Redis port (default: 6379)

Run:
    uvicorn checker.api:app --reload --host 0.0.0.0 --port 8000
"""

import os
import time
import urllib.parse
from typing import Optional

import redis
from fastapi import FastAPI, HTTPException, Query
from pydantic import BaseModel

from .collector import RouteCollector, RouteSnapshot, RouteEntry
from .diff_engine import DiffEngine, Inconsistency


# ---------------------------------------------------------------------------
# App setup
# ---------------------------------------------------------------------------

app = FastAPI(
    title="SONiC Route Consistency Checker",
    description=(
        "Compares routing state across FRR RIB, APP_DB, ASIC_DB, and Kernel FIB. "
        "Detects inconsistencies and exposes them for AI-assisted root cause analysis."
    ),
    version="0.1.0",
)

# In-memory snapshot cache (last collected)
_snapshot: Optional[RouteSnapshot] = None
_snapshot_time: float = 0.0
_CACHE_TTL = 30  # seconds before snapshot is considered stale


def _get_collector() -> RouteCollector:
    host = os.environ.get("SONIC_HOST", "127.0.0.1")
    port = int(os.environ.get("SONIC_REDIS_PORT", "6379"))
    return RouteCollector(host=host, redis_port=port)


def _get_snapshot(force: bool = False) -> RouteSnapshot:
    global _snapshot, _snapshot_time
    if force or _snapshot is None or (time.time() - _snapshot_time) > _CACHE_TTL:
        _snapshot = _get_collector().collect()
        _snapshot_time = time.time()
    return _snapshot


# ---------------------------------------------------------------------------
# Response models
# ---------------------------------------------------------------------------

class HealthResponse(BaseModel):
    status: str
    redis_app_db: str
    redis_asic_db: str
    snapshot_age_seconds: Optional[float] = None


class SnapshotSummary(BaseModel):
    timestamp: float
    app_db_routes: int
    asic_db_routes: int
    frr_routes: int
    kernel_routes: int
    collected_at: str


class RouteEntryModel(BaseModel):
    prefix: str
    nexthops: list[str]
    interfaces: list[str]
    protocol: Optional[str]
    metric: Optional[int]
    vrf: str


class RouteCrossPlane(BaseModel):
    prefix: str
    frr: Optional[RouteEntryModel]
    app_db: Optional[RouteEntryModel]
    asic_db: Optional[RouteEntryModel]
    kernel: Optional[RouteEntryModel]


class InconsistencyModel(BaseModel):
    prefix: str
    vrf: str
    present_in: list[str]
    missing_in: list[str]
    nexthop_mismatch: dict[str, list[str]]
    severity: str
    diagnosis: str


class InconsistenciesResponse(BaseModel):
    snapshot_timestamp: float
    total: int
    critical: int
    warning: int
    info: int
    inconsistencies: list[InconsistencyModel]


class HistoryEvent(BaseModel):
    timestamp: float
    event_type: str   # "hset" or "del"
    prefix: str
    plane: str


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _route_to_model(entry: Optional[RouteEntry]) -> Optional[RouteEntryModel]:
    if entry is None:
        return None
    return RouteEntryModel(
        prefix=entry.prefix,
        nexthops=entry.nexthops,
        interfaces=entry.interfaces,
        protocol=entry.protocol,
        metric=entry.metric,
        vrf=entry.vrf,
    )


def _inconsistency_to_model(i: Inconsistency) -> InconsistencyModel:
    return InconsistencyModel(
        prefix=i.prefix,
        vrf=i.vrf,
        present_in=i.present_in,
        missing_in=i.missing_in,
        nexthop_mismatch=i.nexthop_mismatch,
        severity=i.severity,
        diagnosis=i.diagnosis,
    )


def _find_route_in_plane(
    plane: dict[str, RouteEntry], prefix: str
) -> Optional[RouteEntry]:
    """Lookup with canonical prefix normalization."""
    from ipaddress import ip_network
    try:
        canonical = str(ip_network(prefix, strict=False))
    except ValueError:
        canonical = prefix

    if canonical in plane:
        return plane[canonical]
    for k, v in plane.items():
        try:
            if str(ip_network(k, strict=False)) == canonical:
                return v
        except ValueError:
            pass
    return None


